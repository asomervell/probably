// GetCategorizationQueueStats returns stats about the categorization queue
type CategorizationQueueStats struct {
	Pending    int `json:"pending"`
	Queued     int `json:"queued"`
	Processing int `json:"processing"`
	Done       int `json:"done"`
	Skipped    int `json:"skipped"`
	Failed     int `json:"failed"`
}

func (s *TransactionStore) GetCategorizationQueueStats(ctx context.Context, ledgerID uuid.UUID) (*CategorizationQueueStats, error) {
	stats := &CategorizationQueueStats{}

	rows, err := s.pool.Query(ctx, `
		SELECT categorization_status, COUNT(*) 
		FROM transactions 
		WHERE ledger_id = $1 
		GROUP BY categorization_status
	`, ledgerID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	for rows.Next() {
		var status string
		var count int
		if err := rows.Scan(&status, &count); err != nil {
			return nil, err
		}
		switch status {
		case CategorizationStatusPending:
			stats.Pending = count
		case CategorizationStatusQueued:
			stats.Queued = count
		case CategorizationStatusProcessing:
			stats.Processing = count
		case CategorizationStatusDone:
			stats.Done = count
		case CategorizationStatusSkipped:
			stats.Skipped = count
		case CategorizationStatusFailed:
			stats.Failed = count
		}
	}

	return stats, rows.Err()
}

