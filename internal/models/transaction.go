// GetAllQueuedForCategorization returns transactions from ALL ledgers that need categorization
// Only returns transactions with no tags (uncategorized)
// Also includes transactions stuck in 'processing' status for more than 5 minutes (likely from crashed worker)
func (s *TransactionStore) GetAllQueuedForCategorization(ctx context.Context, limit int, maxRetries int) ([]*Transaction, error) {
	rows, err := s.pool.Query(ctx, `
		SELECT t.id, t.ledger_id, t.date, t.description, t.notes,
			t.teller_transaction_id, t.teller_type, t.teller_category, t.teller_status,
			t.counterparty_name, t.counterparty_type, t.running_balance_cents,
			t.is_transfer, t.transfer_pair_id, t.transfer_type,
			t.categorization_status, t.categorization_error, t.categorization_attempts,
			t.categorization_queued_at, t.categorization_completed_at,
			t.created_at, t.updated_at
		FROM transactions t
		WHERE (
			t.categorization_status IN ('pending', 'queued', 'failed')
			OR (
				t.categorization_status = 'processing'
				AND t.updated_at < NOW() - INTERVAL '5 minutes'
			)
		)
			AND t.is_transfer = false
			AND t.categorization_attempts < $2
			AND NOT EXISTS (SELECT 1 FROM transaction_tags tt WHERE tt.transaction_id = t.id)
		ORDER BY COALESCE(t.categorization_queued_at, t.created_at) ASC
		LIMIT $1
	`, limit, maxRetries)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	return s.scanTransactionsWithCategorization(rows)
}

// UpdateCategorizationStatus updates the categorization status of a transaction
