// SetTransferPair links two transactions as a transfer pair.
// It marks both transactions as transfers, links them, and recategorizes the contra entries
// to "Internal Transfer" accounts. This ensures:
// 1. Both sides are clearly marked as transfers
// 2. Internal Transfer (Income) + Internal Transfer (Expense) nets to $0
// 3. The real account entries (asset/liability) are NEVER modified
func (s *TransactionStore) SetTransferPair(ctx context.Context, txn1ID, txn2ID uuid.UUID) error {
	return pgx.BeginFunc(ctx, s.pool, func(tx pgx.Tx) error {
		// Mark both transactions as transfers and link them
		_, err := tx.Exec(ctx, `
			UPDATE transactions SET is_transfer = true, transfer_pair_id = $2 WHERE id = $1
		`, txn1ID, txn2ID)
		if err != nil {
			return err
		}

		_, err = tx.Exec(ctx, `
			UPDATE transactions SET is_transfer = true, transfer_pair_id = $2 WHERE id = $1
		`, txn2ID, txn1ID)
		if err != nil {
			return err
		}

		// Get ledger ID from one of the transactions
		var ledgerID uuid.UUID
		err = tx.QueryRow(ctx, `SELECT ledger_id FROM transactions WHERE id = $1`, txn1ID).Scan(&ledgerID)
		if err != nil {
			return err
		}

		// Get or create Internal Transfer suspense account (equity type)
		// Using equity means it won't appear in income/expense reports
		// Both sides of a transfer hit this account and net to zero
		var internalTransferID uuid.UUID

		// Try to find existing Internal Transfer equity account
		err = tx.QueryRow(ctx, `
			SELECT id FROM accounts
			WHERE ledger_id = $1 AND name = 'Internal Transfer' AND type = 'equity'
		`, ledgerID).Scan(&internalTransferID)
		if err != nil {
			// Create it if not found
			err = tx.QueryRow(ctx, `
				INSERT INTO accounts (ledger_id, name, type, is_active, created_at, updated_at)
				VALUES ($1, 'Internal Transfer', 'equity', true, NOW(), NOW())
				RETURNING id
			`, ledgerID).Scan(&internalTransferID)
			if err != nil {
				return err
			}
		}

		// Update contra entries (income/expense type accounts) to Internal Transfer
		// We ONLY touch income/expense entries, NEVER asset/liability entries
		// This preserves the real account balances while properly categorizing transfers
		//
		// All transfer contra entries go to the same equity account.
		// The entries will net to zero (one positive, one negative).
		for _, txnID := range []uuid.UUID{txn1ID, txn2ID} {
			// Find the contra entry (income/expense) to update
			var contraEntryID uuid.UUID
			err = tx.QueryRow(ctx, `
				SELECT e.id
				FROM entries e
				JOIN accounts a ON e.account_id = a.id
				WHERE e.transaction_id = $1 AND a.type IN ('income', 'expense')
			`, txnID).Scan(&contraEntryID)
			if err != nil {
				// No contra entry found, skip
				continue
			}

			_, err = tx.Exec(ctx, `UPDATE entries SET account_id = $2 WHERE id = $1`, contraEntryID, internalTransferID)
			if err != nil {
				return err
			}
		}

		return nil
	})
}

// UnlinkTransferPair unlinks two transactions that were marked as a transfer pair.
// It removes the transfer flags and restores contra entries to Uncategorized Income/Expense.
func (s *TransactionStore) UnlinkTransferPair(ctx context.Context, txnID uuid.UUID) error {
	return pgx.BeginFunc(ctx, s.pool, func(tx pgx.Tx) error {
		// Get the paired transaction and ledger ID
		var pairID *uuid.UUID
		var ledgerID uuid.UUID
		err := tx.QueryRow(ctx, `SELECT transfer_pair_id, ledger_id FROM transactions WHERE id = $1`, txnID).Scan(&pairID, &ledgerID)
		if err != nil || pairID == nil {
			return err
		}

		// Get the Uncategorized Income and Expense accounts
		var uncatIncomeID, uncatExpenseID uuid.UUID
		rows, err := tx.Query(ctx, `
			SELECT id, type FROM accounts
			WHERE ledger_id = $1 AND name IN ('Uncategorized Income', 'Uncategorized Expenses')
		`, ledgerID)
		if err != nil {
			return err
		}
		defer rows.Close()
		for rows.Next() {
			var id uuid.UUID
			var accType AccountType
			if err := rows.Scan(&id, &accType); err != nil {
				return err
			}
			if accType == AccountTypeIncome {
				uncatIncomeID = id
			} else if accType == AccountTypeExpense {
				uncatExpenseID = id
			}
		}
		if err := rows.Err(); err != nil {
			return err
		}

		// Restore contra entries from Internal Transfer back to Uncategorized
		// Use asset entry sign to determine the correct uncategorized account
		for _, tid := range []uuid.UUID{txnID, *pairID} {
			// Find the Internal Transfer entry
			var entryID uuid.UUID
			err = tx.QueryRow(ctx, `
				SELECT e.id
				FROM entries e
				JOIN accounts a ON e.account_id = a.id
				WHERE e.transaction_id = $1 AND a.name = 'Internal Transfer'
			`, tid).Scan(&entryID)
			if err != nil {
				// No Internal Transfer entry found, skip
				continue
			}

			// Find the asset/liability entry to determine money flow direction
			var assetEntryAmount int64
			err = tx.QueryRow(ctx, `
				SELECT e.amount_cents
				FROM entries e
				JOIN accounts a ON e.account_id = a.id
				WHERE e.transaction_id = $1 AND a.type IN ('asset', 'liability')
				LIMIT 1
			`, tid).Scan(&assetEntryAmount)
			if err != nil {
				continue
			}

			// Restore to Uncategorized based on asset entry sign:
			// - Positive (money arriving) → Uncategorized Income
			// - Negative (money leaving) → Uncategorized Expenses
			var targetAccountID uuid.UUID
			if assetEntryAmount > 0 && uncatIncomeID != uuid.Nil {
				targetAccountID = uncatIncomeID
			} else if assetEntryAmount < 0 && uncatExpenseID != uuid.Nil {
				targetAccountID = uncatExpenseID
			}

			if targetAccountID != uuid.Nil {
				_, err = tx.Exec(ctx, `UPDATE entries SET account_id = $2 WHERE id = $1`, entryID, targetAccountID)
				if err != nil {
					return err
				}
			}
		}

		// Unlink both transactions
		_, err = tx.Exec(ctx, `
			UPDATE transactions SET is_transfer = false, transfer_pair_id = NULL WHERE id = $1
		`, txnID)
		if err != nil {
			return err
		}

		_, err = tx.Exec(ctx, `
			UPDATE transactions SET is_transfer = false, transfer_pair_id = NULL WHERE id = $1
		`, *pairID)
		return err
	})
}

// TransferCandidate represents a potential transfer match with entry details
type TransferCandidate struct {
	Transaction *Transaction
	AccountID   uuid.UUID
	AccountName string
	AccountType AccountType
	AmountCents int64
}

// FindTransferCandidates finds potential transfer matches for a transaction
// It looks for transactions with matching amounts (opposite sign) within a date window.
//
// All valid transfers have OPPOSITE signs from the bank's perspective:
// - Asset↔Asset (checking→savings): -$100 leaves checking, +$100 arrives in savings
// - Asset↔Liability (checking→credit card): -$100 leaves checking, +$100 payment on CC
// - Cash advance: +$100 arrives in checking, -$100 charge on CC
func (s *TransactionStore) FindTransferCandidates(ctx context.Context, txn *Transaction, amountCents int64, accountID uuid.UUID, dayWindow int) ([]*TransferCandidate, error) {
	// Find transactions with opposite amount in same ledger within date window
	// that are not already marked as transfers.
	//
	// KEY INSIGHT: If an account has both inflow (+) and outflow (-) of the same amount,
	// these are SEPARATE events (e.g., payroll in, bill payment out), NOT a transfer.
	// A real transfer only touches each account ONCE - money leaves A, arrives at B.
	//
	// So we exclude matches if the original account already has an opposite-amount
	// transaction - that would be the natural "pair" within the same account.
	rows, err := s.pool.Query(ctx, `
		SELECT t.id, t.ledger_id, t.date, t.description, t.notes,
			t.teller_transaction_id, t.teller_type, t.teller_category, t.teller_status,
			t.counterparty_name, t.counterparty_type, t.running_balance_cents,
			t.is_transfer, t.transfer_pair_id, t.created_at, t.updated_at,
			e.account_id, e.amount_cents, a.name as account_name, a.type as account_type
		FROM transactions t
		JOIN entries e ON t.id = e.transaction_id
		JOIN accounts a ON e.account_id = a.id
		WHERE t.ledger_id = $1
			AND t.id != $2
			AND t.is_transfer = false
			AND e.amount_cents = $3
			AND e.account_id != $4
			AND t.date BETWEEN $5 AND $6
			-- Only match entries from asset/liability accounts (not expense/income)
			AND a.type IN ('asset', 'liability')
			-- Exclude candidates that have ANY entry in the original account
			AND NOT EXISTS (
				SELECT 1 FROM entries e2
				WHERE e2.transaction_id = t.id
				AND e2.account_id = $4
			)
			-- KEY FIX: Don't match if the original account has another transaction
			-- with the opposite amount (inflow/outflow pair in same account = not a transfer)
			AND NOT EXISTS (
				SELECT 1 FROM entries e_same
				JOIN transactions t_same ON e_same.transaction_id = t_same.id
				WHERE e_same.account_id = $4
				AND e_same.amount_cents = $3
				AND t_same.id != $2
				AND t_same.is_transfer = false
				AND t_same.date BETWEEN $5 AND $6
			)
		ORDER BY ABS(t.date - $7::date), t.created_at DESC
		LIMIT 10
	`, txn.LedgerID, txn.ID, -amountCents, accountID,
		txn.Date.AddDate(0, 0, -dayWindow), txn.Date.AddDate(0, 0, dayWindow), txn.Date)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var candidates []*TransferCandidate
	for rows.Next() {
		var t Transaction
		var notes, tellerTxnID, tellerType, tellerCategory, tellerStatus sql.NullString
		var counterpartyName, counterpartyType sql.NullString
		var runningBalanceCents sql.NullInt64
		var transferPairID *uuid.UUID
		var c TransferCandidate

		if err := rows.Scan(&t.ID, &t.LedgerID, &t.Date, &t.Description, &notes,
			&tellerTxnID, &tellerType, &tellerCategory, &tellerStatus,
			&counterpartyName, &counterpartyType, &runningBalanceCents,
			&t.IsTransfer, &transferPairID, &t.CreatedAt, &t.UpdatedAt,
			&c.AccountID, &c.AmountCents, &c.AccountName, &c.AccountType); err != nil {
			return nil, err
		}

		applyNullTransactionFields(&t, notes, tellerTxnID, tellerType, tellerCategory, tellerStatus, counterpartyName, counterpartyType, runningBalanceCents)
		t.TransferPairID = transferPairID

		c.Transaction = &t
		candidates = append(candidates, &c)
	}

	return candidates, rows.Err()
}

