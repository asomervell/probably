// resetCategorizationByIDs resets categorization status to queued and removes tags for the given IDs.
func (s *TransactionStore) resetCategorizationByIDs(ctx context.Context, txnIDs []uuid.UUID) error {
	now := time.Now()
	result, err := s.pool.Exec(ctx, `
		UPDATE transactions SET
			categorization_status = $1,
			categorization_error = NULL,
			categorization_attempts = 0,
			categorization_queued_at = $2,
			categorization_completed_at = NULL,
			updated_at = $2
		WHERE id = ANY($3)
	`, CategorizationStatusQueued, now, txnIDs)
	if err != nil {
		return err
	}
	slog.InfoContext(ctx, "reset transactions for reprocessing", "count", result.RowsAffected())

	tagResult, err := s.pool.Exec(ctx, `
		DELETE FROM transaction_tags WHERE transaction_id = ANY($1)
	`, txnIDs)
	if err != nil {
		return err
	}
	slog.InfoContext(ctx, "deleted transaction tags", "count", tagResult.RowsAffected())
	return nil
}

// MarkForRecategorization resets transactions to pending status so they get reprocessed.
// Also removes their existing tags so they can be re-categorized fresh.
func (s *TransactionStore) MarkForRecategorization(ctx context.Context, txnIDs []uuid.UUID) error {
	if len(txnIDs) == 0 {
		return nil
	}
	return s.resetCategorizationByIDs(ctx, txnIDs)
}

// MarkAllForRecategorization resets ALL transactions in a ledger for reprocessing
func (s *TransactionStore) MarkAllForRecategorization(ctx context.Context, ledgerID uuid.UUID) (int, error) {
	now := time.Now()

	// Reset categorization status for all non-transfer transactions
	result, err := s.pool.Exec(ctx, `
		UPDATE transactions SET
			categorization_status = $1,
			categorization_error = NULL,
			categorization_attempts = 0,
			categorization_queued_at = $2,
			categorization_completed_at = NULL,
			updated_at = $2
		WHERE ledger_id = $3 AND is_transfer = false
	`, CategorizationStatusQueued, now, ledgerID)
	if err != nil {
		return 0, err
	}

	count := int(result.RowsAffected())

	// Remove existing tags from all transactions in this ledger
	_, err = s.pool.Exec(ctx, `
		DELETE FROM transaction_tags 
		WHERE transaction_id IN (
			SELECT id FROM transactions WHERE ledger_id = $1 AND is_transfer = false
		)
	`, ledgerID)

	return count, err
}

// MarkForRecategorizationByTagName finds all transactions with the given tag name and marks them for recategorization.
// Returns the number of transactions affected.
func (s *TransactionStore) MarkForRecategorizationByTagName(ctx context.Context, ledgerID uuid.UUID, tagName string) (int, error) {
	var tagID uuid.UUID
	err := s.pool.QueryRow(ctx, `
		SELECT id FROM tags
		WHERE ledger_id = $1 AND LOWER(name) = LOWER($2)
	`, ledgerID, tagName).Scan(&tagID)
	if err != nil {
		return 0, err
	}

	rows, err := s.pool.Query(ctx, `
		SELECT transaction_id FROM transaction_tags WHERE tag_id = $1
	`, tagID)
	if err != nil {
		return 0, err
	}
	defer rows.Close()

	var txnIDs []uuid.UUID
	for rows.Next() {
		var id uuid.UUID
		if err := rows.Scan(&id); err != nil {
			return 0, err
		}
		txnIDs = append(txnIDs, id)
	}
	if err := rows.Err(); err != nil {
		return 0, err
	}

	if len(txnIDs) == 0 {
		return 0, nil
	}

	if err := s.resetCategorizationByIDs(ctx, txnIDs); err != nil {
		return 0, err
	}
	return len(txnIDs), nil
}

// MarkAllForRecategorizationByTagName finds all transactions across ALL ledgers with the given tag name
// and marks them for recategorization. Returns the number of transactions affected.
func (s *TransactionStore) MarkAllForRecategorizationByTagName(ctx context.Context, tagName string) (int, error) {
	now := time.Now()

	// Find all tag IDs with this name (case-insensitive) across all ledgers
	rows, err := s.pool.Query(ctx, `
		SELECT id FROM tags WHERE LOWER(name) = LOWER($1)
	`, tagName)
	if err != nil {
		return 0, err
	}
	defer rows.Close()

	var tagIDs []uuid.UUID
	for rows.Next() {
		var id uuid.UUID
		if err := rows.Scan(&id); err != nil {
			return 0, err
		}
		tagIDs = append(tagIDs, id)
	}
	if err := rows.Err(); err != nil {
		return 0, err
	}

	if len(tagIDs) == 0 {
		return 0, nil
	}

	// Get all transaction IDs with any of these tags
	txnRows, err := s.pool.Query(ctx, `
		SELECT DISTINCT transaction_id FROM transaction_tags WHERE tag_id = ANY($1)
	`, tagIDs)
	if err != nil {
		return 0, err
	}
	defer txnRows.Close()

	var txnIDs []uuid.UUID
	for txnRows.Next() {
		var id uuid.UUID
		if err := txnRows.Scan(&id); err != nil {
			return 0, err
		}
		txnIDs = append(txnIDs, id)
	}
	if err := txnRows.Err(); err != nil {
		return 0, err
	}

	if len(txnIDs) == 0 {
		return 0, nil
	}

	// Reset categorization and P2P transfer type status
	// This puts them back in the categorization queue which will detect P2P transfers
	_, err = s.pool.Exec(ctx, `
		UPDATE transactions SET
			transfer_type = NULL,
			categorization_status = $1,
			categorization_error = NULL,
			categorization_attempts = 0,
			categorization_queued_at = $2,
			categorization_completed_at = NULL,
			updated_at = $2
		WHERE id = ANY($3)
	`, CategorizationStatusQueued, now, txnIDs)
	if err != nil {
		return 0, err
	}

	// Remove existing tags from these transactions
	_, err = s.pool.Exec(ctx, `
		DELETE FROM transaction_tags WHERE transaction_id = ANY($1)
	`, txnIDs)
	if err != nil {
		return 0, err
	}

	return len(txnIDs), nil
}

