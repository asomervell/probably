// ExistingPattern holds pattern data that can be inherited by new transactions
type ExistingPattern struct {
	PatternType string
	Frequency   string
	PatternName string
	Confidence  int
}

// GetExistingPatternForEntity finds an existing pattern for an entity
// Returns the most recent, high-confidence pattern that can be inherited
func (s *TransactionStore) GetExistingPatternForEntity(ctx context.Context, entityID uuid.UUID, ledgerID uuid.UUID) (*ExistingPattern, error) {
	var patternType string
	var metadata []byte

	err := s.pool.QueryRow(ctx, `
		SELECT pattern_type, pattern_metadata
		FROM transactions
		WHERE entity_id = $1
			AND ledger_id = $2
			AND pattern_type IS NOT NULL
			AND pattern_type NOT IN ('none', '', 'dismissed')
			AND pattern_detection_status = 'done'
			AND COALESCE((pattern_metadata->>'confidence')::INT, 0) >= 70
		ORDER BY date DESC
		LIMIT 1
	`, entityID, ledgerID).Scan(&patternType, &metadata)

	if err != nil {
		return nil, err
	}

	pattern := &ExistingPattern{
		PatternType: patternType,
	}

	// Extract metadata fields if available
	if len(metadata) > 0 {
		var meta map[string]interface{}
		if err := json.Unmarshal(metadata, &meta); err == nil {
			if freq, ok := meta["frequency"].(string); ok {
				pattern.Frequency = freq
			}
			if name, ok := meta["pattern_name"].(string); ok {
				pattern.PatternName = name
			}
			if conf, ok := meta["confidence"].(float64); ok {
				pattern.Confidence = int(conf)
			}
		}
	}

	return pattern, nil
}

// InheritPattern copies pattern metadata from an existing pattern to a new transaction
// This allows single transactions to immediately become part of established patterns
func (s *TransactionStore) InheritPattern(ctx context.Context, txnID uuid.UUID, pattern *ExistingPattern) error {
	now := time.Now()

	// Build metadata with inherited flag
	metadata := map[string]interface{}{
		"inherited":    true,
		"frequency":    pattern.Frequency,
		"pattern_name": pattern.PatternName,
		"confidence":   pattern.Confidence,
	}

	metadataJSON, err := json.Marshal(metadata)
	if err != nil {
		return fmt.Errorf("failed to marshal inherited pattern metadata: %w", err)
	}

	_, err = s.pool.Exec(ctx, `
		UPDATE transactions SET
			pattern_type = $2,
			pattern_metadata = $3,
			pattern_detection_status = $4,
			updated_at = $5
		WHERE id = $1
	`, txnID, pattern.PatternType, metadataJSON, PatternDetectionStatusDone, now)

	return err
}

